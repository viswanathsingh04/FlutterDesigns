              height: 20,
            ),
            buildStackedImages(direction: TextDirection.rtl),
            Text(_chosenDateTime != null
                ? _chosenDateTime.toString()
                : 'No date time picked!'),
            CupertinoButton(
              child: Text('Show Picker'),
              onPressed: () => _showDatePicker(context),
            )
